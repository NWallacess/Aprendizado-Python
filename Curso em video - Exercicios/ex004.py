n=input('digite algo: ')
print('O tipo primitivo desse valor é', type(n))
print('Só tem espaço? \033[1;32m{}\033[m'.format(n.isspace()))
print('É um numero? \033[1;32m{}\033[m'.format(n.isnumeric()))
print('É alphabetico? \033[1;32m{}\033[m'.format(n.isalpha()))
print('É alphnumerico? \033[1;32m{}\033[m'.format(n.isalnum()))
print('É em maiusculo? \033[1;32m{}\033[m'.format(n.isupper()))
print('É em minusculo? \033[1;32m{}\033[m'.format(n.islower()))
print('É capitalizado? \033[1;32m{}\033[m'.format(n.istitle()))